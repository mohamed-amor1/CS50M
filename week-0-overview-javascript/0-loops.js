// for loop
for (let i = 0; i < 5; i++) {
  // code to execute on each iteration
}

// while loop
while (condition) {
  // code to execute while condition is true
}

// do-while loop
do {
  // code to execute at least once, then repeat while condition is true
} while (condition);

// break statement
for (let i = 0; i < 10; i++) {
  if (i === 5) {
    break; // exit the loop when i is equal to 5
  }
}
